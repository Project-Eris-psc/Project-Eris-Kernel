int mtk_mdp_frvc_process(struct mtk_mdp_ctx *ctx)
{
	struct mdp_process_vsi *vsi;
	struct mdp_config *src_cfg, *dst_cfg;
	struct cmdq_pkt *pkt;
	int ret;
	int support = 1;

	vsi = ctx->vpu.vsi;
	src_cfg = &vsi->src_config;
	dst_cfg = &vsi->dst_config;
	pkt = ctx->cmdq_handle;
	pkt->cmd_buf_size = 0;

	if (src_cfg->format == DP_COLOR_YUYV &&
	    src_cfg->x == 0 &&
	    src_cfg->y == 0 &&
	    src_cfg->crop_x == 0 &&
	    src_cfg->crop_y == 0 &&
	    dst_cfg->x == 0 &&
	    dst_cfg->y == 0 &&
	    dst_cfg->crop_x == 0 &&
	    dst_cfg->crop_y == 0 &&
	    vsi->misc.hflip == 0 &&
	    vsi->misc.vflip == 0) {

		if (dst_cfg->format == DP_COLOR_YUYV &&
		    src_cfg->crop_w == 1280 && src_cfg->crop_h == 720 &&
		    dst_cfg->crop_w == 1920 && dst_cfg->crop_h == 1080 &&
		    vsi->misc.orientation == 0)
			yuyv_1280x720_yuyv_1920x1080(pkt,
				&vsi->src_buffer, &vsi->dst_buffer);
		else if (dst_cfg->format == DP_COLOR_RGBA8888 &&
		    src_cfg->crop_w == 1280 && src_cfg->crop_h == 720 &&
		    dst_cfg->crop_w == 1920 && dst_cfg->crop_h == 1080 &&
		    vsi->misc.orientation == 0)
			yuyv_1280x720_argb32_1920x1080(pkt,
				&vsi->src_buffer, &vsi->dst_buffer);
		else if (dst_cfg->format == DP_COLOR_BGRA8888 &&
		    src_cfg->crop_w == 1280 && src_cfg->crop_h == 720 &&
		    dst_cfg->crop_w == 1920 && dst_cfg->crop_h == 1080 &&
		    vsi->misc.orientation == 0)
			yuyv_1280x720_abgr32_1920x1080(pkt,
				&vsi->src_buffer, &vsi->dst_buffer);
		else
			support = 0;

	} else if (src_cfg->format == DP_COLOR_420_BLKP &&
	    src_cfg->x == 0 &&
	    src_cfg->y == 0 &&
	    src_cfg->crop_x == 0 &&
	    src_cfg->crop_y == 0 &&
	    dst_cfg->x == 0 &&
	    dst_cfg->y == 0 &&
	    dst_cfg->crop_x == 0 &&
	    dst_cfg->crop_y == 0 &&
	    vsi->misc.hflip == 0 &&
	    vsi->misc.vflip == 0) {

		if (dst_cfg->format == DP_COLOR_YUYV &&
		    src_cfg->crop_w == 720 && src_cfg->crop_h == 480 &&
		    dst_cfg->crop_w == 1920 && dst_cfg->crop_h == 1080 &&
		    vsi->misc.orientation == 0)
			mt21_720x480_yuyv_1920x1080(pkt,
				&vsi->src_buffer, &vsi->dst_buffer);
		else if (dst_cfg->format == DP_COLOR_RGBA8888 &&
		    src_cfg->crop_w == 720 && src_cfg->crop_h == 480 &&
		    dst_cfg->crop_w == 1920 && dst_cfg->crop_h == 1080 &&
		    vsi->misc.orientation == 0)
			mt21_720x480_argb32_1920x1080(pkt,
				&vsi->src_buffer, &vsi->dst_buffer);
		else if (dst_cfg->format == DP_COLOR_BGRA8888 &&
		    src_cfg->crop_w == 720 && src_cfg->crop_h == 480 &&
		    dst_cfg->crop_w == 1920 && dst_cfg->crop_h == 1080 &&
		    vsi->misc.orientation == 0)
			mt21_720x480_abgr32_1920x1080(pkt,
				&vsi->src_buffer, &vsi->dst_buffer);
		else if (dst_cfg->format == DP_COLOR_YUYV &&
		    src_cfg->w == 1920 && src_cfg->h == 1088 &&
		    src_cfg->crop_w == 1920 && src_cfg->crop_h == 1080 &&
		    dst_cfg->w == 1920 && dst_cfg->h == 1080 &&
		    dst_cfg->crop_w == 1920 && dst_cfg->crop_h == 1080 &&
		    vsi->misc.orientation == 0)
			mt21_1920x1080_yuyv_1920x1080(pkt,
				&vsi->src_buffer, &vsi->dst_buffer);
		else
			support = 0;

	} else if (src_cfg->format == DP_COLOR_NV16 &&
	    src_cfg->x == 0 &&
	    src_cfg->y == 0 &&
	    src_cfg->crop_x == 0 &&
	    src_cfg->crop_y == 0 &&
	    dst_cfg->x == 0 &&
	    dst_cfg->y == 0 &&
	    dst_cfg->crop_x == 0 &&
	    dst_cfg->crop_y == 0 &&
	    vsi->misc.hflip == 0 &&
	    vsi->misc.vflip == 0) {

		if (dst_cfg->format == DP_COLOR_YUYV &&
		    src_cfg->crop_w == 720 && src_cfg->crop_h == 480 &&
		    dst_cfg->crop_w == 1920 && dst_cfg->crop_h == 1080 &&
		    vsi->misc.orientation == 0)
			nv16_720x480_yuyv_1920x1080(pkt,
				&vsi->src_buffer, &vsi->dst_buffer);
		else if (dst_cfg->format == DP_COLOR_RGBA8888 &&
		    src_cfg->crop_w == 720 && src_cfg->crop_h == 480 &&
		    dst_cfg->crop_w == 1920 && dst_cfg->crop_h == 1080 &&
		    vsi->misc.orientation == 0)
			nv16_720x480_argb32_1920x1080(pkt,
				&vsi->src_buffer, &vsi->dst_buffer);
		else if (dst_cfg->format == DP_COLOR_BGRA8888 &&
		    src_cfg->crop_w == 720 && src_cfg->crop_h == 480 &&
		    dst_cfg->crop_w == 1920 && dst_cfg->crop_h == 1080 &&
		    vsi->misc.orientation == 0)
			nv16_720x480_abgr32_1920x1080(pkt,
				&vsi->src_buffer, &vsi->dst_buffer);
		else
			support = 0;

	} else
		support = 0;

	if (support == 1) {
		ret = cmdq_pkt_flush(ctx->mdp_dev->cmdq_client, pkt);
		if (ret < 0)
			mtk_mdp_err("cmdq flush failed!!!\n");
	} else {
		ret = -1;
		mtk_mdp_dbg(0, "frvc unsupport:");
		mtk_mdp_dbg(0, "in: (%d, %d, %d, %d, %d, %d, %08x)\n",
			src_cfg->x, src_cfg->y, src_cfg->w, src_cfg->h,
			src_cfg->crop_w, src_cfg->crop_h, src_cfg->format);
		mtk_mdp_dbg(0, "out: (%d, %d, %d, %d, %d, %d, %08x)\n",
			dst_cfg->x, dst_cfg->y, dst_cfg->w, dst_cfg->h,
			dst_cfg->crop_w, dst_cfg->crop_h, dst_cfg->format);
		mtk_mdp_dbg(0, "misc: (%d, %d, %d)\n",
			vsi->misc.hflip, vsi->misc.vflip,
			vsi->misc.orientation);
	}

	return ret;
}
EXPORT_SYMBOL(mtk_mdp_frvc_process);

