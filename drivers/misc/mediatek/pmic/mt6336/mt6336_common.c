/* write one flag register */
unsigned short mt6336_set_flag_register_value(MT6336_PMU_FLAGS_LIST_ENUM flagname, unsigned char val)
{
	const MT6336_PMU_FLAG_TABLE_ENTRY *pFlag = &mt6336_pmu_flags_table[flagname];
	unsigned int ret = 0;

	if (pFlag->flagname != flagname) {
		pr_err("[%s]pmic flag idx error\n", __func__);
		return 1;
	}
	ret = mt6336_config_interface((unsigned int) (pFlag->offset), (unsigned char) (val),
		(unsigned char) (pFlag->mask), (unsigned char) (pFlag->shift));

	return 0;
}

unsigned short mt6336_get_flag_register_value(MT6336_PMU_FLAGS_LIST_ENUM flagname)
{
	const MT6336_PMU_FLAG_TABLE_ENTRY *pFlag = &mt6336_pmu_flags_table[flagname];
	unsigned char val;
	unsigned int ret;

	ret = mt6336_read_interface((unsigned int) pFlag->offset, &val,
		(unsigned int) (pFlag->mask), (unsigned int) (pFlag->shift));

	return val;
}
