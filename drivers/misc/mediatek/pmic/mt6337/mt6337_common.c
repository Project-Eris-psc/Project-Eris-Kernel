unsigned short mt6337_set_register_value(MT6337_PMU_FLAGS_LIST_ENUM flagname, unsigned int val)
{
	const MT6337_PMU_FLAG_TABLE_ENTRY *pFlag = &mt6337_pmu_flags_table[flagname];
	unsigned int ret = 0;

	if (pFlag->flagname != flagname) {
		pr_err("[%s]pmic flag idx error\n", __func__);
		return 1;
	}
	ret = mt6337_config_interface((pFlag->offset), (unsigned int)(val),
		(unsigned int)(pFlag->mask), (unsigned int)(pFlag->shift));
	if (ret != 0) {
		pr_err("[%s] error ret: %d when set register value: %d\n", __func__, ret, val);
		return ret;
	}

	return 0;
}


int mt6337_get_register_value(MT6337_PMU_FLAGS_LIST_ENUM flagname)
{
	const MT6337_PMU_FLAG_TABLE_ENTRY *pFlag = &mt6337_pmu_flags_table[flagname];
	unsigned int val;
	unsigned int ret;

	ret = mt6337_read_interface((unsigned int)pFlag->offset, &val,
		(unsigned int)(pFlag->mask), (unsigned int)(pFlag->shift));
	if (ret != 0) {
		pr_err("[%s] error ret: %d\n", __func__, ret);
		return -1;
	}

	return val;
}
